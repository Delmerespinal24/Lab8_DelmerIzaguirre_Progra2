    public void Mostrar() {
        adminBinarios AB = new adminBinarios("./Contactos.lab");
        AB.CargarArchivo();

        listacontactos = AB.getListaPersonas();
        DefaultTreeModel m = (DefaultTreeModel) jt_agenda.getModel();
        DefaultMutableTreeNode raiz = (DefaultMutableTreeNode) m.getRoot();

        raiz.removeAllChildren();

        DefaultComboBoxModel cb_mensaje = (DefaultComboBoxModel) cb_contactos.getModel();
        cb_mensaje.removeAllElements();
        
        DefaultListModel buzon = (DefaultListModel) jl_buzon.getModel();
        
        for (Contacto lc : listacontactos) {
            cb_mensaje.addElement(lc);
            
            for (Mensaje msn : lc.mensajes) {
                buzon.addElement(msn);
                
            }

            DefaultMutableTreeNode contacto = new DefaultMutableTreeNode(lc);
            DefaultMutableTreeNode E = new DefaultMutableTreeNode("Edad");
            DefaultMutableTreeNode N = new DefaultMutableTreeNode("Numero");
            DefaultMutableTreeNode C = new DefaultMutableTreeNode("Correo");
            DefaultMutableTreeNode D = new DefaultMutableTreeNode("Direccion");
            DefaultMutableTreeNode G = new DefaultMutableTreeNode("Genero");
            E.add(new DefaultMutableTreeNode(lc.getEdad()));
            N.add(new DefaultMutableTreeNode(lc.getNumero()));
            C.add(new DefaultMutableTreeNode(lc.getCorreo()));
            D.add(new DefaultMutableTreeNode(lc.getDireccion()));
            G.add(new DefaultMutableTreeNode(lc.getGenero()));

            contacto.add(E);
            contacto.add(N);
            contacto.add(C);
            contacto.add(D);
            contacto.add(G);

            raiz.add(contacto);

        }

        m.reload();

    }

