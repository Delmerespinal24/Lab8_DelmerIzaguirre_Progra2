    private void orden_edadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orden_edadActionPerformed
        // TODO add your handling code here:
        Contacto[] listaedades = new Contacto[listacontactos.size()];
        for (int i = 0; i < listacontactos.size(); i++) {
            listaedades[i] = listacontactos.get(i);
        }
        Contacto aux;

        for (int i = 0; i < listaedades.length - 1; i++) {
            for (int x = i + 1; x < listaedades.length; x++) {
                if (listaedades[x].getEdad() < listaedades[i].getEdad()) {
                    aux = listaedades[i];
                    listaedades[i] = listaedades[x];
                    listaedades[x] = aux;
                }
            }
        }
        listacontactos = new ArrayList();
        for (int i = 0; i < listaedades.length; i++) {
            listacontactos.add(listaedades[i]);
        }
        Subir();


    }//GEN-LAST:event_orden_edadActionPerformed

    private void orden_generoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orden_generoActionPerformed
        // TODO add your handling code here:
        Contacto[] listageneros = new Contacto[listacontactos.size()];
        for (int i = 0; i < listacontactos.size(); i++) {
            listageneros[i] = listacontactos.get(i);
        }
        Contacto aux;

        for (int i = 0; i < listageneros.length - 1; i++) {
            for (int x = i + 1; x < listageneros.length; x++) {
                if (listageneros[x].getGenero().equals("Masculino") && listageneros[i].getGenero().equals("Femenino")) {
                    aux = listageneros[i];
                    listageneros[i] = listageneros[x];
                    listageneros[x] = aux;
                }
            }
        }
        listacontactos = new ArrayList();
        for (int i = 0; i < listageneros.length; i++) {
            listacontactos.add(listageneros[i]);
        }
        Subir();
    }//GEN-LAST:event_orden_generoActionPerformed

